/**
 * JavaPattern
 * 
 * This class implements a pattern in a particular format 
 * using J's and A's. 
 */
public class JavaPattern {
  public static void main(String[] args) {

  	// Prints the first line of the pattern
    System.out.println("    J    A    V     V   A");

    // Prints the second line of the pattern
    System.out.println("    J   A A    V   V   A A");
   
    // Prints the third line of the Java pattern
    System.out.println("J   J  AAAAA    V V   AAAAA");

    /* Prints the fourth and final line of the java pattern which 
     * completes the pattern
     */
    System.out.println(" J J  A     A    V   A     A");
  }
}
 